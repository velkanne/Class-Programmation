# Clase 11: Tuplas y Conjuntos

# Las tuplas son como las listas, pero inmutables (no se pueden cambiar).
coordenadas = (10.0, 20.5)
print(f"Tupla: {coordenadas}")
print(f"Latitud: {coordenadas[0]}")

# Los conjuntos son colecciones desordenadas de elementos únicos.
numeros_unicos = {1, 2, 3, 2, 1, 4}
print(f"Conjunto (elementos duplicados eliminados): {numeros_unicos}")
